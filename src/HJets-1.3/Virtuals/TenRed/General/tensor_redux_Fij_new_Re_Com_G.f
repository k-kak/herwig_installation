c                                                             d^4k 
c 
c   F0; F_mu; F_mu,nu; F_mu,nu,rho; F_mu,nu,rho_sig  =  Int ------- 
c                                                           (2pi)^4 
c 
c   1; k_mu; k_mu k_nu; k_mu k_nu k_rho; k_mu k_nu k_rho k_sig 
c -------------------------------------------------------------------------- 
c   k^2  (k+p1)^2  (k+p1+p2)  (k+p1+p2+p3)^2  (k+p1+p2+p3+p4)^2 (k+p1+p2+p3+p4+p5)^2 
c 
c  with 
c 
c   F_mu = p1_mu E11  +  p2_mu E21  +  p3_mu E31  +  p4_mu E41 +  p5_mu E51 
c 
c   F_munu = p1_mu p1_nu E12 + p2_mu p2_nu E22 + ... 
c 
c 
C INPUT:  p1sq, p1p2,...                     kinematics invariants 
c         E0_13456 = E0(p1+p2,p3,p4,p5,p6)          scalar 5-point functions 
c         E0_12456 = E0(p1,p2+p3,p4,p5,p6)          in the PV notation 
c         E0_12356 = E0(p1,p2,p3+p4,p5,p6) 
c         E0_12346 = E0(p1,p2,p3,p4+p5,p6)    
c         E0_12345 = E0(p1,p2,p3,p4,p5+p6)       
c         E0_23456 = E0(p2,p3,p4,p5,p1+p6) 
 
C         Eij_12356(n,m) = E_nm(1,2,3,5) ....    higher E_nm form factors 
C                                               as in tens_red5 and tens_red4 
c 
c OUTPUT: Fij(n,m) = F_nm                    form factors in the tensor  
c                                            integrals a la D-D 
c         nm = 11, 21, 31, 51                ff's for E_mu 
c         nm = 12, 22, 32, ...,  162          ff's for E_munu 
c         nm = 13, 23, 33, ...,  403     ff's for E_mu,nu,rho 
c         nm = 14, 24, 34, ...,  864 
c         nm = 15, 25, 35, ..., 1665 
